Tolerancia
Es admitir y respetar en los demás una manera de ser, de obrar o pensar distinta a la propia, sin renunciar a nuestras propias convicciones y sin caer en la indiferencia.

Compromiso
Es poner amor y empeño en el cumplimiento de nuestros deberes, para la satisfacción personal y el engrandecimiento de nuestra institución.

Responsabilidad
Cumplir a cabalidad con todos nuestros deberes y compromisos, asumiendo las consecuencias de nuestras acciones.

Solidaridad
Es la determinación firme y perseverante de considerar como propias las dificultades de los demás, buscando la forma de cooperar, creer y progresar juntos.

Honestidad
Es ser recto, honrado y transparente en todas nuestras acciones y en todos los acontecimientos de la vida.
