                <p>DON JOSÉ MIGUEL  DE RESTREPO Y PUERTA, Presidente por dos períodos del Estado Soberano de Antíoquía. Por: Miguel Ángel Cuenca Quintero
del Centro de Historia "Heriberto Zapata Cuéncar" de Copacabana. No se ha podido encontrar el retrato de este ilustre copacabanita. Le hemos buscado en Santa	
Fé de Antioquia y en las pinacotecas de Medellín, sin resultados.	

Nacido en Copacabana en 1755, hijo de Don José Ignacio y Doña María Antonia, bautizado el tercer Cura Propio del Sitio,  Doctor Carlos de Puerta y Molina, quien era su tío materno.	

Estudió en Medellín,  ciudad de la que fue Concejal por varios años, Teniente de Gobernación,
Alcalde y Presidente del Censo.   Fue de los firmantes de la  Constitución del Estado en 1812.
Casó allí con Doña LeonorVélez del Ribero.
Pasaron  a Vivir en Envigado, y allí procrearon a Don José Manuel  Restrepo Vélez, el Historiador
de Colombia y Secretario de Bolívar, Gobernador de Antioquia y fundador de la educación en
Copacabana.
<br><br>
En  1812, por  muerte del  primer  Presidente  del  Estado  Soberano  de Antioquia,  Don  José
Antonio Gómez le sucedió en el cargo, y fue reelegido para un segundo período.
Entre sus ejecutorias se cuentan su reconocimiento al Congreso de la Nueva Granada, su bella y encendida  proclama a los  antioqueños incitándoles a defender la Patria amenazada  desde eJ Sur por Sámano, y su polémica con Don Antonio Na riño condenando el centralismo.
En vista del peligro de la reconquista española, recomendó  entregar el mando a persona  capaz de organizar la defensa del territorio, y así advino Don Juan del Corral a gobernar con poderes dictatoriales como lo demandaba el momento histórico.
Varios intelectuales  opinan  que  todos los actos admirables de Del Corral en el gobierno de Antioquia no fueron sus ideas, sino el fruto del estudio que un grupo de próceres y notables antioqueños venía haciendo,  de tiempo  atrás encabezados  por el copacabanita  Don  José Miguel  de Restrepo y Puerta y su hijo el envigadeño Don José Manuel Restrepo Vélez.
Nuestro paisano De Restrepo y Puerta, al dejar el mando, se dedicó a la agricultura y la minería, primero en Angostura, Antioquia, y luego en Suesca, Cundinamarca, en donde  murió y fue sepultado el 1º. De Octubre de 1829.</p>                    
