<p>HISTORIA DE LA EDUCACIÓN EN COPACABANA
LA ESCUELA DE NIÑOS   DESPUÉS JOSÉ MIGUEL DE RESTREPO Y PUERTA
Po Miguel Ángel Cuenca Quintero, del Centro de Historia  "Heriberto Zapata Cuéncar".
La  primera Educación se impartió desde los hogares. Los Curas Párrocos mantenían, en las Sacristías y Casas Curales, la formación de candidatos al sacerdocio católico, los que iban luego a presentar exámenes y a ordenarse en Popayán, Diócesis  a la que pertenecíamos.
La  primera escuela primara del Sitio de Nuestra Señora de Copacabana de la Tasajera existió por los lados de El Cabuyal, en 1814, dirigida por Don Cruz Congote, un señor, agricultor, venido de San Jerónimo. Es  mencionada  en el informativo oficial  sobre la viabilidad  de la Escuela Pública  en Copacabana,  proceso  que  se  inició  por  mandato oficial,  que  encabezaban,  en Bogotá el General  Francisco de Paula Santander,  Vicepresidente de la Nación, y en Medellín el Doctor José Manuel Restrepo, gobernador de Antioquia.
En  1820 se funda La Junta  Curadora de la  Educación en Copacabana, con el Alcalde Pedáneo Don Pedro Montoya, El Cura Doctor Carlos José Cadavid,  dos padres de familia Gabriel lsaza y Cosme Jaramillo y dos regidores o concejales de Medellín,  lo que demuestra a las claras que aún   no  éramos  municipio,    Agustín   Londoño  y  otro.  Se  presentó  una  terna  para  el nombramiento de nuestro  maestro de primeras letras,  pero  los ternados no llenaban  los requisitos mínimos para el cargo. Se retrasó el proyecto de escuela pública.
<br><br>
El 23 de Marzo de 1821 el Gobernador Don José Manuel Restrepo, hijo de Don José Miguel de Restrepo y Puerta, nombró como primer maestro para Copacabana a  Don Juan José Tirado, quien se posesionó.
Pero  la  escuela no la  iba  a  pagar  el gobierno,  sino  los  padres  de familia,  quienes  no  tenían dinero  en efectivo, por lo que  el primer maestro de Copacabana se murió de hambre, porque ni los padres de familia ni el gobierno le pagaron sus sueldos.
La Escuela Pública de Niños del Sitio de Copacabana funcionó a intervalos irregulares, dadas las guerras civiles en el país, y las polémicas de los Maestros con los Curas Párrocos por razón de los  pensums  impuestos desde Bogotá  que  minimizaban  la  enseñanza  religiosa.  La  misma escuela oficial funcionó  en varias casas arrendadas, hasta que el prohombre cívico de Copacabana,  Don  Segundo  Fonnegra  lsaza,  rico y muy  generoso,  regaló  un terreno para construir la escuela pública, cercenando su propio solar de su casa de habitación en el parque principal, que iba, desde el actual palacio municipal, hasta el río Medellín.  Además construyó a sus expensas el bello local que conocimos, patrimonio entrañable de Copacabana, que cayó derribado por la pica del progreso en 1994, para construir lo que se conoce hoy con el nombre de Complejo Educativo.</p>
